        <h2>Hunger Game</h2>
        <p>Le hunger game est un battle royal avec pour thème le célèbre film. Pour lancer une partie, il faudra être minimum 4 joueurs. Une fois le jeu lancé et après un décompte, il faudra vous rendre au centre des plateformes pour accéder aux coffres et récupérer des items. Le pvp est désactivé les 30 premières secondes du jeu. Ensuite, il vous faudra survivre et être le dernier en vie.</p>
        <h2>Bed war</h2> 
    <p>Dans Bed Wars, les joueurs apparaissent en équipes de quatre joueurs maximum, avec huit ou quatre équipes différentes sur des îles séparées selon les modes. Ces îles ont un lit à l’avant et un générateur de ressources à l’arrière. Ce générateur de ressources fait apparaître du fer et de l’or, ou des émeraudes avec l’amélioration Forge d’émeraude, et ces ressources peuvent être utilisées à la boutique d’objets pour acheter des objets de plusieurs catégories.
    Les joueurs apparaissent avec une épée en bois et une boussole dans leur inventaire. L’épée en bois sera toujours dans l’inventaire d’un joueur à moins qu’une autre épée ne soit acquise, et apparaîtra si l’autre épée est retirée de l’inventaire du joueur. La boussole ouvre un menu pour les communications d’équipe ou le suivi des joueurs. Les communications d’équipe font apparaître une sélection de messages prédéfinis qui peuvent être sélectionnés pour diffuser le message aux autres membres de l’équipe, tandis que le suivi des joueurs suit les joueurs d’une équipe sélectionnée si tous les lits adverses sont détruits.
    Les joueurs peuvent se rendre sur d’autres îles de l’équipe et casser leurs lits pour éliminer la méthode de reproduction de l’équipe. Les équipes peuvent défendre le lit en plaçant des blocs par-dessus, avec une sélection de blocs disponibles dans la boutique. Si le lit de l’équipe est cassé par un joueur d’une autre équipe, il ne se reproduira plus et sera éliminé du jeu à sa mort.
    Les joueurs peuvent visiter différentes îles autour de la carte, qui abritent des générateurs uniques d’une seule ressource. Une île de diamants est située entre deux îles d’une équipe et fait apparaître des diamants qui peuvent être utilisés pour acheter des avantages d’équipe à la boutique de mise à niveau. Plus loin de l’île aux Diamants se trouve l’île du milieu, avec des générateurs d’émeraudes qui font apparaître des émeraudes. Ces Émeraudes peuvent être utilisées à la boutique d’objets pour acheter des objets plus forts et uniques.
    Différents événements se déroulent au fil du temps, notamment la mise à niveau des générateurs de diamants et d’émeraudes, la destruction des lits, l’apparition du dragon et le jeu se terminant par un point d’apparition. Lorsque tous les lits et les joueurs adverses sont éliminés, la dernière équipe en lice remporte le jeu.</p>
    <h2>Dé à coudre</h2>
    <p>Il faut sauter dans l'eau. A chaque saut, le joueur pose un bloc sous ses pieds de façon à « combler » le bloc d'eau sur lequel il est tombé. Au fur et à mesure que le jeu continue, les cases d'eau sont de moins en moins nombreuses. Il faut donc sauter avec de plus en plus de précision pour éviter de tomber sur les blocs posés et ne pas mourir, afin de rester le dernier en vie.     
Les joueurs peuvent réaliser un dé à coudre. En outre, si le joueur parvient à tomber dans un trou de 1x1 entre les blocs posés par les joueurs. Une fois, réalisé, le joueur obtient un bonus qui peut varier (Une vie en plus)
</p>
